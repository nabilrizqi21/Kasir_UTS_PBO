    private void totalHarga() {
        int banyak = TabelData.getRowCount();
        int hasil = 0;
        for (int i = 0; i < banyak; i++) {
            hasil += Integer.parseInt(TabelData.getValueAt(i, 4).toString());
        }
        if (hasil > 200000) {
            int disc = (int) (hasil * 0.15);
            hasil -= disc;
            txtDisc.setText("Disc. 30%");
        }
        txtTotal.setText(String.valueOf(hasil));
    }
